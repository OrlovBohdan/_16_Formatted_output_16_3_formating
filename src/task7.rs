fn main() {
    let s = "Hello, world!";

    // Виводимо перші 5 символів рядка `s` (отримуємо "Hello")
    println!("{0:.5}", s); // => Hello

    // Використовується форматування рядка. Тут виникає помилка, оскільки `format!` очікує лише один аргумент
    // У наступному рядку ми підставимо "abcdefg", але через відсутність другого параметра, це буде невірно
    assert_eq!(format!("Hello {}!", "abc"), "Hello abc!"); // Виправлено з 3 на "abc"

    println!("Success!");
}





